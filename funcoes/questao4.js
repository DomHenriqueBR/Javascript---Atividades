function palavraNaFrase(frase, palavra) {
    return frase.includes(palavra);
}

console.log(palavraNaFrase("Aquele que habita o esconderijo do Altíssimo, à sombra do Onipresente", "descansará"));
console.log(palavraNaFrase("Direi do senhor: Ele é o meu Deus, o meu refúgio, a minha fortaleza, e nele", "confiarei")); 
